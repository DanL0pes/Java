
public class Calculadora {
	double n1, n2, res;
	public void soma() {
		res=n1+n2;
		System.out.println("A soma é "+res);
	}
	public void sub() {
		res=n1-n2;
		System.out.println("A diferença é "+res);
	}
	public void mult() {
		res=n1*n2;
		System.out.println("O produto é "+res);
	}
	public void div() {
		res=n1/n2;
		System.out.println("O quociente é "+res);
	}
}
